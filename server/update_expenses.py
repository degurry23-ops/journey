for tid, expenses in mappings.items():
    idx = content.find("id: '" + tid + "'")
    if idx < 0:
        print(f'{tid}: NOT FOUND')
        continue
    exp_start = content.find('expenses:', idx)
    cnt = 0; started = False; end_idx = exp_start
    for i in range(exp_start, len(content)):
        if content[i] == '[': cnt += 1; started = True
        elif content[i] == ']':
            cnt -= 1
            if started and cnt == 0: end_idx = i + 1; break

    new_block = '    expenses: [\n' + ',\n'.join(expenses) + '\n    ]'
    old_block = content[exp_start:end_idx]
    content = content.replace(old_block, new_block)
    print(f'{tid}: {len(expenses)} expenses')

with open('server/seed.js', 'w', encoding='utf-8') as f:
    f.write(content)
